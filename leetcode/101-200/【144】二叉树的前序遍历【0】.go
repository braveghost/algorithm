func PreorderTraversal_Iter(root *TreeNode) []int {

	var stack []*TreeNode
	var res []int
	for root != nil || len(stack) > 0{
		for root != nil{
			res = append(res, root.Val)
			stack = append(stack, root.Right)
			root = root.Left
		}
		root = stack[len(stack)-1]
		stack = stack[:len(stack)-1]
	}
	return res
}
