/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */


type TreeNode struct {
     Val int
 Left *TreeNode
	   Right *TreeNode
}

func PreorderTraversal(root *TreeNode) []int {
	var preorder func(node *TreeNode)
	var res []int
	preorder = func(node *TreeNode) {
		if node == nil {
			return
		}
		res = append(res, node.Val)
		preorder(node.Left)
		preorder(node.Right)
	}
	preorder(root)
	return res
}
